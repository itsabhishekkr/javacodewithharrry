public class Typepermotion {
    public static void main(String args[]){
        short e=5;
        byte f=25;
        char g='d';
        byte bt=(byte)(e+f+g);
        System.out.println(bt);

        int a=10;
        float b=20.2762f;
        long c=25;
        double ans=a+b+c;
        System.out.println(ans);

        byte z=5;
        byte x=(byte)(z*2);
        System.out.println(x);
    }
    
}
