public class typecasting2 {
    public static void main(String args[]){
        float a=4858.566f;
        int b=(int)a;
        System.out.println(a);
        System.out.println(b);
        char ch='c';
        char ch1='d';
        int num1=ch;
        int num2=ch1;
        System.out.println(num1);
        System.out.println(num2);
    }
    
}
